        void llenarTabla(){
        Conexion con = new Conexion();
        try {
            con.getConexion();
            Statement stmt = con.getConexion().createStatement();
            ResultSet rs = stmt.executeQuery("SELECT * FROM proyecto");
            DefaultTableModel value = new DefaultTableModel();
            tblSeleccionarObra_Arriendo.setModel(value);
            value.addColumn("CODIGO PROYECTO");
            value.addColumn("NOMBRE PROYECTO");
            value.addColumn("DESCRIPCION PROYECTO");
            while(rs.next()){
               value.addRow(new Object[]{rs.getString("ID_PROY"), rs.getString("NOMBRE_PROY"),rs.getString("DESCRIPCION_PROY")});
            }
            rs.close();
        } catch (Exception e) {
            System.out.println(e);
        }
    }

