    private void btnBuscarProyecto_EditMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnBuscarProyecto_EditMouseClicked
        
        Conexion con = new Conexion();
        
        try{
            con.getConexion();
            Statement stmt = con.getConexion().createStatement();
            try{
                ResultSet rs = stmt.executeQuery("SELECT * FROM PROYECTO WHERE ID_PROY ='"+txtCodEditarProyecto.getText()+"'");
                while(rs.next()){
                    txtNombreProyecto_Edit.setText(rs.getString("NOMBRE_PROY"));
                    txtDescripcionProyecto_Edit.setText(rs.getString("DESCRIPCION_PROY"));
                }
            }catch (SQLException ex){
                System.out.println(ex);
            }
        } catch (SQLException ex) {
            System.out.println(ex);
        }
        

        
    }//GEN-LAST:event_btnBuscarProyecto_EditMouseClicked

