    private void btnBuscarMatMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnBuscarMatMouseClicked
        // TODO add your handling code here:
        Conexion con = new Conexion();
        
        try{
            con.getConexion();
            Statement stmt = con.getConexion().createStatement();
            try{
                ResultSet rs = stmt.executeQuery("SELECT * FROM MATERIAL WHERE ID_MAT ='"+txtIdMaterial.getText()+"'");
                while(rs.next()){
                    txtNombreMatEdit.setText(rs.getString("NOMBRE_MAT"));
                    txtStockMaterialEdit.setText(rs.getString("STOCK_MAT"));
                }       
                
            } catch (SQLException ex){
                
                System.out.println(ex);
            }            
        } catch (SQLException ex){
            System.out.println(ex);
        }
        
    }//GEN-LAST:event_btnBuscarMatMouseClicked

