    void llenarTabla(){
        Conexion con = new Conexion();
        try{
            con.getConexion();
            Statement stmt = con.getConexion().createStatement();
            ResultSet rs = stmt.executeQuery("SELECT * FROM material");
            DefaultTableModel value = new DefaultTableModel();
            tblListadoMateriales.setModel(value);
            value.addColumn("CODIGO MATERIAL");
            value.addColumn("NOMBRE");
            value.addColumn("STOCK");
            while(rs.next()){
                value.addRow(new Object[]{rs.getString("ID_MAT"), rs.getString("NOMBRE_MAT"), rs.getInt("STOCK_MAT")});
            }
            rs.close();
        } catch(SQLException e){
            System.out.println(e);
        }
        
        
    }
    
